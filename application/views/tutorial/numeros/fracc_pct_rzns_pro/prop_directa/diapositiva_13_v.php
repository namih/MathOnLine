		<p>En el ejemplo anterior, la cantidad de medicamento en polvo y la cantidad de mililitros de agua están relacionadas 
			de manera <b>proporcional</b>. En este tipo de relaciones, cuando una cantidad (por ejemplo el medicamento en polvo) 
			aumenta <i>al doble</i>, la cantidad correspondiente (el agua) <i>también aumenta al doble</i>. Si la primera 
			cantidad aumenta <i>al triple</i> o aumenta cuatro o cinco veces, la cantidad correspondiente aumenta <i>al triple</i> 
			o aumenta cuatro o cinco veces. Cuando la cantidad disminuye, por ejemplo a <i>la mitad</i>, la cantidad 
			correspondiente también <i>disminuye a la mitad</i>.</p>
		<p>En una situacion de proporcionalidad, al valor en un conjunto (por ejemplo miligramos de medicina) que corresponde 
			<b>a una unidad</b> del otro conjunto (por ejemplo un mililitro de agua) se le llama <b>valor unitario</b>.</p>
